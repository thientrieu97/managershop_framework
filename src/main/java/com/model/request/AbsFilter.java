package com.model.request;

public abstract class AbsFilter {

    protected int page;

    protected int size;

    protected AbsFilter() {
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }
}
